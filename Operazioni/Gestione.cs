namespace Operazioni
{
    public class Gestione
    {
        public static int Somma(int a,int b)
        {            
            return a+b;
        }
        public static double SommaDouble(double a, double b)
        {
            return a + b;
        }
        public static int Differenza(int a, int b)
        {
            return a - b;
        }
        public static double DifferenzaDouble(double a, double b)
        {
            return a - b;
        }
        public static int Moltiplicazione(int a, int b)
        {
            return a * b;
        }
        public static double MoltiplicazioneDouble(double a, double b)
        {
            return a * b;
        }
        public static int Divisione(int a, int b)
        {
            return a / b;
        }
        public static double DivisioneDouble(double a, double b)
        {
            return a / b;
        }
    }
}